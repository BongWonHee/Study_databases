CREATE TABLE RECHECKUP
(
  RECHECKUP         VARCHAR(500) NOT NULL COMMENT '재검진',
  RECHECKUP_ID      VARCHAR(500) NOT NULL COMMENT '재검진ID',
  RECHECKUP_LIST_ID VARCHAR(500) NOT NULL COMMENT '재검진항목ID',
  PRIMARY KEY (RECHECKUP_ID)
