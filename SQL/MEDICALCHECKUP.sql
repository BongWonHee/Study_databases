
CREATE TABLE BLOOD_P
(
  BLOOD_P    INTEGER NOT NULL COMMENT '혈압',
  BLOOD_P_ID INTEGER NOT NULL COMMENT '혈압ID',
  PRIMARY KEY (BLOOD_P_ID)
) COMMENT '혈압';

CREATE TABLE BLOOD_S
(
  BLOOD_S    INTEGER NOT NULL COMMENT '혈당',
  BLOOD_S_ID INTEGER NOT NULL COMMENT '혈당ID',
  PRIMARY KEY (BLOOD_S_ID)
) COMMENT '혈당';

CREATE TABLE GENDER
(
  GENDER    VARCHAR(50) NOT NULL COMMENT '성별',
  GENDER_ID VARCHAR(50) NOT NULL COMMENT '성별ID',
  PRIMARY KEY (GENDER_ID)
) COMMENT '성별';

CREATE TABLE IMPROVMENT
(
  IMPROVMENT    VARCHAR(500) NOT NULL COMMENT '유지방안',
  IMPROVMENT_ID VARCHAR(500) NOT NULL COMMENT '유지방안ID',
  PRIMARY KEY (IMPROVMENT_ID)
) COMMENT '유지방안';

CREATE TABLE MEDICALCHECKUP
(
  MEDICALCHECKUP    VARCHAR(500) NOT NULL COMMENT '검진',
  MEDICALCHECKUP_ID VARCHAR(500) NOT NULL COMMENT '검진ID',
  NAME_ID           VARCHAR(500) NOT NULL COMMENT '이름ID',
  GENDER_ID         VARCHAR(50)  NOT NULL COMMENT '성별ID',
  TALL_WEIGHT_ID    VARCHAR(500) NOT NULL COMMENT '키ID',
  BLOOD_P_ID        INTEGER      NOT NULL COMMENT '혈압ID',
  RESULT_ID         VARCHAR(500) NOT NULL COMMENT '검진결과ID',
  WEIGHT_ID         VARCHAR(500) NOT NULL COMMENT '몸무게ID',
  BLOOD_S_ID        INTEGER      NOT NULL COMMENT '혈당ID',
  PRIMARY KEY (MEDICALCHECKUP_ID)
