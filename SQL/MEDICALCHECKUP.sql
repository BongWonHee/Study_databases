  RECHECKUP_LIST    VARCHAR(500) NOT NULL COMMENT '재검진항목',
  RECHECKUP_LIST_ID VARCHAR(500) NOT NULL COMMENT '재검진항목ID',
  PRIMARY KEY (RECHECKUP_LIST_ID)
) COMMENT '재검진항목';

CREATE TABLE RESULT
(
  RESULT_VALUE    VARCHAR(500) NOT NULL COMMENT '검진결과',
  RESULT_VALUE_ID VARCHAR(500) NOT NULL COMMENT '검진결과ID',
  RECHECKUP_ID    VARCHAR(500) NOT NULL COMMENT '재검진ID',
  IMPROVMENT_ID   VARCHAR(500) NOT NULL COMMENT '유지방안ID',
  PRIMARY KEY (RESULT_VALUE_ID)
) COMMENT '검진결과';

CREATE TABLE TALL
(
